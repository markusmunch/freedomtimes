import { readFileSync, writeFileSync } from 'node:fs';
import { resolve } from 'node:path';

const legacyBase = resolve('agents/uk-and-europe-cults-columnist');
const targetSql = resolve('agents/cult-columnist-cf-worker/migrations/0002_seed_config.sql');

const readJson = (relativePath) => JSON.parse(readFileSync(resolve(legacyBase, relativePath), 'utf-8'));
const sqlString = (value) => `'${String(value).replace(/'/g, "''")}'`;

const cultTerms = readJson('cult-terms.json');
const genericCultTerms = readJson('data/generic-cult-terms.json');
const strictExtensions = readJson('data/strict-cult-term-extensions.json');
const figurativeContext = readJson('data/figurative-cult-context-terms.json');
const figurativeCommercial = readJson('data/figurative-cult-commercial-context-terms.json');
const figurativePhrases = readJson('data/figurative-cult-phrases.json');
const figurativePatterns = readJson('data/figurative-cult-patterns-by-language.json');
const stopwords = readJson('data/group-stopwords-by-language.json');
const allowedHosts = readJson('allowed-source-hosts.json');
const excludedHosts = readJson('data/excluded-source-hosts.json');
const watchlistSites = readJson('watchlist-sites.json');
const feeds = readJson('feeds.json').feeds;

const lines = [];
lines.push('-- Migration: 0002_seed_config');
lines.push('-- Seeds D1 config tables from legacy JSON sources.');
lines.push('-- Generated by scripts/generate-seed-migration.mjs');
lines.push('');
lines.push('BEGIN TRANSACTION;');
lines.push('');

lines.push('-- reset config tables for deterministic seeds');
lines.push('DELETE FROM feeds;');
lines.push('DELETE FROM source_hosts;');
lines.push('DELETE FROM cult_terms;');
lines.push('DELETE FROM figurative_terms;');
lines.push('DELETE FROM group_stopwords;');
lines.push('');

for (const feed of feeds) {
  lines.push(
    `INSERT INTO feeds (id, title, url, source_format, source_category, language, requires_url_resolution, url_resolver, enabled) VALUES (${sqlString(feed.id)}, ${sqlString(feed.title)}, ${sqlString(feed.url)}, ${sqlString(feed.sourceFormat)}, ${sqlString(feed.sourceCategory)}, ${sqlString(feed.language ?? 'en')}, ${feed.requiresUrlResolution ? 1 : 0}, ${feed.urlResolver ? sqlString(feed.urlResolver) : 'NULL'}, ${feed.enabled ? 1 : 0});`,
  );
}
lines.push('');

const seenHosts = new Set();
const insertHost = (host, listType) => {
  const normalized = host.toLowerCase();
  if (seenHosts.has(normalized)) return;
  seenHosts.add(normalized);
  lines.push(`INSERT INTO source_hosts (host, list_type) VALUES (${sqlString(normalized)}, '${listType}');`);
};
for (const host of allowedHosts) insertHost(host, 'allowed');
for (const host of excludedHosts) insertHost(host, 'excluded');
for (const host of watchlistSites) insertHost(host, 'watchlist');
lines.push('');

const addLanguageTerms = (map, termType, table) => {
  for (const [language, terms] of Object.entries(map)) {
    for (const term of terms) {
      lines.push(
        `INSERT OR IGNORE INTO ${table} (term, language, term_type) VALUES (${sqlString(term)}, ${sqlString(language)}, ${sqlString(termType)});`,
      );
    }
  }
};

addLanguageTerms(cultTerms, 'cult_term', 'cult_terms');
addLanguageTerms(genericCultTerms, 'generic_term', 'cult_terms');
addLanguageTerms(strictExtensions, 'strict_extension', 'cult_terms');
lines.push('');

addLanguageTerms(figurativeContext, 'context_term', 'figurative_terms');
addLanguageTerms(figurativeCommercial, 'commercial_term', 'figurative_terms');
addLanguageTerms(figurativePhrases, 'phrase', 'figurative_terms');
addLanguageTerms(figurativePatterns, 'regex_pattern', 'figurative_terms');
lines.push('');

for (const [language, words] of Object.entries(stopwords)) {
  for (const word of words) {
    lines.push(
      `INSERT OR IGNORE INTO group_stopwords (word, language) VALUES (${sqlString(word)}, ${sqlString(language)});`,
    );
  }
}

lines.push('');
lines.push('COMMIT;');
lines.push('');

writeFileSync(targetSql, `${lines.join('\n')}\n`, 'utf-8');

console.log(`Wrote ${targetSql}`);
console.log(`feeds=${feeds.length}, allowed=${allowedHosts.length}, excluded=${excludedHosts.length}, watchlist=${watchlistSites.length}`);
